package capitulo04_estruturas;

public class Introducao_EstruturasRepeticao {
	public static void main(String[] args) {
		//numero aleatorio em Java (entre 0 e 1)
		double n1 = Math.random();
		System.out.println(n1);
		
		//numero aleatório entre 0 e 100
		double n2 = Math.random() * 100;
		System.out.println(n2);
		
		//numero inteiro aleatório entre 0 e 100
		int n3 = (int)(Math.random() * 100);
		System.out.println(n3);
		
		//numero aleatório inteiro entre 20 e 80
		int n4 = 20 + (int)(Math.random() * 60);
		System.out.println(n4);
	}
}






