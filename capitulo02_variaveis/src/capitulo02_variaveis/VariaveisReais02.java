package capitulo02_variaveis;

public class VariaveisReais02 {
	public static void main(String[] args) {
		
		double valor = 123.89;
		int reais = (int)valor;
		
		System.out.println("Valor: " + valor);
		System.out.println("Parte inteira: " + reais);
		System.out.println("Soma: " + (valor + reais));
		
	}
}
