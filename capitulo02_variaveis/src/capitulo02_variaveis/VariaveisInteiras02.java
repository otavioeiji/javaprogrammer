package capitulo02_variaveis;

public class VariaveisInteiras02 {
	public static void main(String[] args) {
		
		//variáveis inteiras: int
		int n1 = 2147483647, n2 = 200;
		int soma = n1 + n2;
		System.out.println("Soma: " + soma);
		
		//variaveis inteiras: byte
		byte b1 = 100, b2 = 28;
		byte total = (byte)(b1 + b2);
		System.out.println("Total: " + total);
	}
}
