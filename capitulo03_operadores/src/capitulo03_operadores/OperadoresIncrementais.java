package capitulo03_operadores;

public class OperadoresIncrementais {
	public static void main(String[] args) {
		
		int n1 = 10, n2 = 10;
		System.out.println("n1 = " + n1);
		System.out.println("n2 = " + n2);
		
		n1++;
		++n2;
		
		System.out.println("n1 = " + n1);
		System.out.println("n2 = " + n2);		
	}
}
